    private void addJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addJButtonActionPerformed
        if(jTextField1.getText().isEmpty() || organizationJComboBox.getSelectedItem()==null){
             JOptionPane.showMessageDialog(null, "Please enter the details of organization!");
             addJButton.setEnabled(false);
             return;
        }
        if(checkIfOrgIsUnique(jTextField1.getText())){
            if(enterpriseType.getValue().equalsIgnoreCase("Vet Hospital Enterprise")){
                VetHospitalType type = (VetHospitalType) organizationJComboBox.getSelectedItem();
                directory.createOrganizationHospital(type,jTextField1.getText());
            }
            if(enterpriseType.getValue().equalsIgnoreCase("Merchant Enterprise")){
                MerchantType type = (MerchantType) organizationJComboBox.getSelectedItem();
                directory.createOrganizationSupplier(type,jTextField1.getText());
            }
            if(enterpriseType.getValue().equalsIgnoreCase("NGO Enterprise")){
                NGOType type = (NGOType) organizationJComboBox.getSelectedItem();
                directory.createOrganizationNgo(type,jTextField1.getText());
            }
            
            if(enterpriseType.getValue().equalsIgnoreCase("Pet Training School Enterprise")){
                PetSchoolType type = (PetSchoolType) organizationJComboBox.getSelectedItem();
                directory.createOrganizationPetSchool(type,jTextField1.getText());
            }
            
             dB4OUtil.storeSystem(system);
            populateTable();
            jTextField1.setText("");
        }
        else{
            JOptionPane.showMessageDialog(null, "Please enter unique details! Organisation already exists");
        }
    }//GEN-LAST:event_addJButtonActionPerformed

    public Boolean checkIfOrgIsUnique(String name){
        return directory.getOrganizationList().stream().noneMatch(org->org.getName().equalsIgnoreCase(name));
    }
    
