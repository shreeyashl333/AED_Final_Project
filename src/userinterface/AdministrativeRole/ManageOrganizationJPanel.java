    private void populateCombo(){
        organizationJComboBox.removeAllItems();
        
        String changetype = enterpriseType.getValue();
        if(changetype.equalsIgnoreCase("Vet Hospital Enterprise")){
            for(VetHospitalType type:Organization.VetHospitalType.values()){
                 if (!type.getValue().equals(VetHospitalType.Admin.getValue()))
                    organizationJComboBox.addItem(type);
            }
        }
        if(changetype.equalsIgnoreCase("Merchant Enterprise")){
            for(MerchantType type:Organization.MerchantType.values()){
                 if (!type.getValue().equals(MerchantType.Admin.getValue()))
                    organizationJComboBox.addItem(type);
            }
        }
        if(changetype.equalsIgnoreCase("NGO Enterprise")){
            for(NGOType type:Organization.NGOType.values()){
                 if (!type.getValue().equals(NGOType.Admin.getValue()))
                    organizationJComboBox.addItem(type);
            }
        }
        
        if(changetype.equalsIgnoreCase("Pet Training School Enterprise")){
            for(PetSchoolType type:Organization.PetSchoolType.values()){
                 if (!type.getValue().equals(PetSchoolType.Admin.getValue()))
                    organizationJComboBox.addItem(type);
            }
        }
       
        organizationJComboBox.setSelectedIndex(-1);
    }

    private void populateTable(){
        DefaultTableModel model = (DefaultTableModel) organizationJTable.getModel();
        
        model.setRowCount(0);
        
        for (Organization organization : directory.getOrganizationList()){
            Object[] row = new Object[2];
            row[0] = organization.getOrganizationID();
            row[1] = organization;
            model.addRow(row);
        }
    }
